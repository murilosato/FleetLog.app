
const CACHE_NAME = 'ecoscheck-v1';
const ASSETS = [
  '/',
  '/index.html',
  '/index.tsx',
  'https://fonts.googleapis.com/css2?family=Inter:wght@300;400;500;600;700&display=swap'
];

self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => cache.addAll(ASSETS))
  );
});

self.addEventListener('fetch', event => {
  // Ignora chamadas para Supabase e Google AI (queremos que falhem normalmente ou sejam tratadas pelo App.tsx)
  if (event.request.url.includes('supabase.co') || event.request.url.includes('googleapis.com')) {
    return;
  }

  event.respondWith(
    caches.match(event.request).then(response => {
      return response || fetch(event.request);
    })
  );
});
